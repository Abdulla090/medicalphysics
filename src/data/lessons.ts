export const getLessonsByCategory = (categoryId: string): Lesson[] => {
  return lessons.filter(lesson => lesson.category === categoryId);
};

export const getRelatedLessons = (lessonId: string, category: string): Lesson[] => {
  return lessons.filter(lesson => lesson.category === category && lesson.id !== lessonId).slice(0, 3);
};
